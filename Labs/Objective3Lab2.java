public class Objective3Lab2 {
  public static void main(String[] args) {
    double side1, side2, hypotenuse, hypotenuseResult;
    side1 = 5.5;
    side2 = 7.5;
    hypotenuse = ((side1 * side1) + (side2 * side2));
    hypotenuseResult = Math.sqrt(hypotenuse);
    System.out.println(hypotenuseResult);
  }
}
